using namespace std;
const int inf = 3e5;
using ll=long long ;
ll mod = 1e9 + 7;

//------------------------------------------------------------------------------

void solve(void)
{
    int n;
    cin>>n;
    vector<ll>v;
    for(int i=0;i<n;i++)
    {
        ll o;
        cin>>o;
        v.push_back(o);
    }
    sort(v.begin(),v.end());
    int f;
    cin>>f;
    int lo=0,hi=n-1,mi,ans=n;
    while (lo<=hi)
    {
        mi=(lo+hi)/2;
        if(v[mi]>=f){
            ans=mi;
            hi=mi-1;
        }
        else
        {
            lo=mi+1;
        }
    }
    
    cout<<ans<<'\n';
}


